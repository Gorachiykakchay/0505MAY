# Часть 1

k = int(input("Введите количество разных фруктов: "))
n = int(input("Введите количество фруктов в одном полднике: "))
menu = [[]]
for i in range(n):
    new_menu = []
    for j in range(len(menu)):
        for m in range(k):
            new_menu.append(menu[j] + [m+1])
    menu = new_menu

for m in menu:
    print([f"Фрукт {fruit}" for fruit in m])

